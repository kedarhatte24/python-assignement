#casting to integer
float_num = 15.75
str_num = "10"
cast_int1 = int(float_num) 
cast_int2 = int(str_num)    
print(cast_int1)
print(cast_int2)

# 2. Casting to Float
int_num = 42
str_pi = "3.14"
cast_float1 = float(int_num) 
cast_float2 = float(str_pi)  
print(cast_float1)
print(cast_float2)

